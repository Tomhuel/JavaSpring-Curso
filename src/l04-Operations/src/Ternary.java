public class Ternary {
    public static void execute() {
        String weekDay = 7 == 5 ? "Domingo" : "Lunes";
        System.out.println("weekDay = " + weekDay);

        double mark = 5.0;

        boolean pass = mark >= 5.0 ? true : false;

        System.out.println("pass = " + pass);
    }
}
