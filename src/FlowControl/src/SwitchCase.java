public class SwitchCase {
    public static void execute() {
        int i = 4;
        switch (i) {
            case 1:
                System.out.println("First");
                break;
            case 2:
                System.out.println("Second");
                break;
            case 3:
                System.out.println("Third");
                break;
            default:
                System.out.println("Default");
        }
    }
}
