public class IncrementalDecremental {
    public static void execute() {
        int i = 5;
        System.out.println("i++ = " + i++);
        System.out.println("i = " + i);
        System.out.println("++i = " + ++i);

        System.out.println("i-- = " + i--);
        System.out.println("i = " + i);
        System.out.println("--i = " + --i);
    }
}
