public class EspecialCharacters {

    public static void execute() {

        // Especial Characters

        char space = ' ';
        char backSpace = '\b';
        char tab = '\t';
        char newLine = '\n';
        char reverseSlash = '\\';
        char simpleComma = '\'';

        System.out.println("Space char: '" + space + "'");
        System.out.println("BackSpace char: '" + backSpace + "'");
        System.out.println("Tab char: '" + tab + "'");
        System.out.println("New Line char: '" + newLine + "'");
        System.out.println("Reverse Slash char: '" + reverseSlash + "'");
        System.out.println("Simple comma char: '" + simpleComma + "'");
    }
}
