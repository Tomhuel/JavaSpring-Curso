public class ArrayExample {
    public static void execute() {
        int[] numbers = new int[4];

        numbers[0] = 3;
        numbers[1] = 7;
        numbers[2] = 5;
        numbers[3] = 1;

        int i = numbers[0];
        int j = numbers[1];
        int k = numbers[2];
        int l = numbers[3];
        System.out.println("i = " + i);
        System.out.println("j = " + j);
        System.out.println("k = " + k);
        System.out.println("l = " + l);
    }
}
